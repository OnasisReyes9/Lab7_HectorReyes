    private void btn_añadirClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_añadirClienteActionPerformed
        // TODO add your handling code here:
        if (jT_nombreCliente.getText().equals("") || jT_apellidoCliente.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Porfavor llene todos los campos.");
        } else if (jCbox_asignarCarroCliente.getItemCount() <= 0) {
            JOptionPane.showMessageDialog(this, "No existen carros para poder crear un cliente.");
        } else if (jCbox_asignarCarroCliente.getSelectedIndex() == 0
                && jCbox_asignarCarroCliente.getItemCount() > 0) {
            JOptionPane.showMessageDialog(this, "Porfavor Selecciona un carro.");
        } else {
            DefaultTableModel tblModel = (DefaultTableModel) jTbl_tablaClientes.getModel();
            DefaultComboBoxModel cBoxmodel = (DefaultComboBoxModel) jCbox_carrosCliente.getModel();
            Cliente c = new Cliente(jT_nombreCliente.getText(),
                    jT_apellidoCliente.getText(),
                    Integer.parseInt(jSp_edadCliente.getValue().toString()));
            c.añadirCarro((Carro) jCbox_asignarCarroCliente.getSelectedItem());

            AdministrarCliente adminC = new AdministrarCliente("./clientes.123");
            adminC.cargarArchivo();
            adminC.añadirCliente(c);
            adminC.escribirArchivo();//*/

            cBoxmodel.addElement(c);
            jCbox_carrosCliente.setModel(cBoxmodel);

            String[] nRow = {jT_nombreCliente.getText(),
                jT_apellidoCliente.getText(),
                jSp_edadCliente.getValue().toString(),
                jCbox_asignarCarroCliente.getSelectedItem().toString() + ","
            };
            jCbox_asignarCarroCliente.removeItemAt(jCbox_asignarCarroCliente.getSelectedIndex());
            tblModel.addRow(nRow);
            jTbl_tablaClientes.setModel(tblModel);
            JOptionPane.showMessageDialog(this, "Se ha añadido un cliente.");
            jT_nombreCliente.setText("");
            jT_apellidoCliente.setText("");
        }
    }//GEN-LAST:event_btn_añadirClienteActionPerformed

