    private void btn_guardarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarEmpleadoActionPerformed
        // TODO add your handling code here:
        if (jT_nombreEmpleado.getText().equals("") || jT_apellido.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Porfavor llene todos los campos.");
        } else if (jTbl_carroParaEmpleado.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un carro para el empleado.");
        } else {
            DefaultComboBoxModel model = (DefaultComboBoxModel) jCbox1.getModel();
            Empleado e = new Empleado(
                    jT_nombreEmpleado.getText(),
                    jT_nombreEmpleado.getText(),
                    Integer.parseInt(jSp_edadEmpleado.getValue().toString()));
            AdministrarEmpleado adminE = new AdministrarEmpleado("./empleados.123");
            numEmpleados++;
            adminE.cargarArchivo();
            adminE.añadirEmpleado(e);
            adminE.escribirArchivo();//*/

            model.addElement(e);
            jCbox1.setModel(model);
            jCbox2.setModel(model);
            jCbox3.setModel(model);

            jTbl_carroParaEmpleado.getSelectedRow();

            String placa = jTbl_carroParaEmpleado.getValueAt(0, 0).toString();
            String tamaño = jTbl_carroParaEmpleado.getValueAt(0, 1).toString();
            int puertas = Integer.parseInt(jTbl_carroParaEmpleado.getValueAt(0, 2).toString());
            int suciedad = Integer.parseInt(jTbl_carroParaEmpleado.getValueAt(0, 3).toString());
            Carro c = new Carro(placa, tamaño, puertas, suciedad);
            e.añadirCarroLavar(c);
            JOptionPane.showMessageDialog(this, "Se ha añadido un empleado.");
            jT_nombreEmpleado.setText("");
            jT_apellido.setText("");
        }
    }//GEN-LAST:event_btn_guardarEmpleadoActionPerformed

    private void btb_aggCarrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btb_aggCarrosActionPerformed
        // TODO add your handling code here:
        if (jCbox_carrosCliente.getSelectedIndex() >= 1) {
            DefaultTableModel model = (DefaultTableModel) jTbl_carroParaEmpleado.getModel();
            Cliente c = (Cliente) jCbox_carrosCliente.getSelectedItem();
            for (int i = 0; i < c.getCarros().size(); i++) {
                Object[] o = {
                    c.getCarros().get(i).getPlaca(),
                    c.getCarros().get(i).getTamaño(),
                    c.getCarros().get(i).getNumPuertas(),
                    c.getCarros().get(i).getNvlSucio()
                };
                model.addRow(o);
            }//fin for
            jTbl_carroParaEmpleado.setModel(model);
            JOptionPane.showMessageDialog(this, "Se ha añadido un carro.");
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un Cliente");
        }
    }//GEN-LAST:event_btb_aggCarrosActionPerformed

